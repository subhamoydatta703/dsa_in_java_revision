package strings;

public class StringConcatenation {
    public static void main(String[] args) {
        // String concatenation
        String str1 = "Hello\t";
        String str2 = "World";
        System.out.print(str1 + str2);
        System.out.println();
        System.out.print(str1 + "Rahul");
        System.out.println();
        System.out.print("DSA" + " in " + "Java");
    }
}
